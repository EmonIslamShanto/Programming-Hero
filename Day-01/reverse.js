// while loop
var num = 1;
while(num <= 10){
    console.log(num);
    num++;
}

// reverse while loop
var num = 10;
while(num > 0){
    console.log(num);
    num--;
}

// for loop
for(var i = 1; i <=10 ; i++){
    console.log(i);
}

// reverse for loop
for(var i = 10; i > 0; i--){
    console.log(i);
}

